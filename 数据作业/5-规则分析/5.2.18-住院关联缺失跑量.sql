    机构编码, 机构名称, 科室名称, 医生姓名, 身份证号, 姓名, 性别, 年龄, 就医日期, 就医天数, 疾病诊断, 分类, 代码,
    名称, 规格, 单位, 单价, 人次, 天数, 频次, 剂量, 数量, 医疗金额, 医保支付, 支付日期)
select '住院' 就医来源,
    '诊疗' 项目来源,
    '规则分析' 线索来源,
    '诊疗关联缺失' 问题类型,
    '该诊疗计费缺失' || t.主项名称 || '主项目' 问题情形,
    '关联缺失' 问题性质,
    数量 问题数量, 
    列支 问题金额,
    机构编码,机构名称,住院科室,住院医生,身份证号,姓名,性别,年龄,住院日期,住院天数,疾病诊断,
    类别, 代码, 名称, 规格, 单位, 单价, 人次, 天数, 频次, 剂量, 数量, 金额, 列支, 日期
from 临时_住院诊疗 g inner join 规则_诊疗关联 t on upper(g.代码) like t.主项编码 || '%' and g.名称 like '%' || t.主项名称 || '%'
    and not exists (select 1 from 临时_计费项目 s where g.机构编码 = s.机构编码 and g.身份证号 = s.身份证号 and g.日期 = s.日期 
    and upper(g.代码) like s.诊疗编码 || '%' and regexp_instr(g.名称,s.诊疗名称) > 0)
order by 住院科室,身份证号,住院日期,名称;
