--*******************************************************************************************--
--脚本：从门诊、住院结算负荷表中归集药品、材料计费数据
drop table 临时_物资销售 purge;

create table 临时_物资销售 as 
select 机构编码,机构名称,业务周期,代码,类别,属性,名称,规格,
       SUM(数量) 销售数量,SUM(金额) 销售金额,
       trunc(sum(数量*单价) / sum(数量),2) 销售均价,单位 销售单位
from(
SELECT 机构编码,机构名称,to_char(费用发生日期,'YYYYMM') as 业务周期,国标代码 代码,
DECODE(收费项目类别,'西药费','西药','成药费','成药','草药费','饮片','材料') 类别,医疗属性类别 属性,
商品名 名称,规格,数量,金额,单价,数量单位 单位 FROM 负荷_住院结算
WHERE 机构编码 = 'H00000000000' and 收费项目类别 IN ('西药费','成药费','草药费','材料费')
and substr(upper(国标代码),1,1) in ('C','X','Z')
union all
SELECT 机构编码,机构名称,to_char(费用发生日期,'YYYYMM') as 业务周期,国标代码 代码,
DECODE(收费项目类别,'西药费','西药','成药费','成药','草药费','饮片','材料') 类别,医疗属性类别 属性,
商品名 名称,规格,数量,金额,单价,数量单位 单位 FROM 负荷_门诊结算
WHERE 机构编码 = 'H00000000000' and 收费项目类别 IN ('西药费','成药费','草药费','材料费')
and substr(upper(国标代码),1,1) in ('C','X','Z')
) a
GROUP BY 机构编码,机构名称,业务周期,代码,类别,属性,名称,规格,单价,单位
having sum(数量) > 0;

--脚本：从药库、药房消耗负荷表中归集药品、材料购入数据
drop table 临时_物资购入 purge;

create table 临时_物资购入 as 
select 机构编码,机构名称,代码,拆零比,
       sum(数量) as 购入数量,sum(数量*单价) as 购入金额,trunc(sum(数量*单价) / sum(数量),2) as 购入均价, 
       单位 购入单位, 业务期间
from(
select 机构编码,机构名称,国标代码 as 代码,拆零比,
       数量,数量*购入价 as 金额,购入价 单价, 包装单位 as 单位, to_char(业务日期,'YYYYMM') as 业务期间
from 负荷_药库消耗 where 机构编码 = 'H00000000000' and 业务类型 in ('物资采购')
and substr(upper(国标代码),1,1) in ('C','X','Z')
union all
select 机构编码,机构名称,国标代码 as 代码,拆零比,
       数量,数量*购入价 as 金额,购入价 单价, 包装单位 as 单位, to_char(业务日期,'YYYYMM') as 业务期间
from 负荷_药房消耗 where 机构编码 = 'H00000000000' and 业务类型 in ('物资采购')
and substr(upper(国标代码),1,1) in ('C','X','Z')
) a
group by 机构编码,机构名称,代码,拆零比,单位,业务期间
having sum(数量) <> 0;


--脚本：从药库、药房结转负荷表中归集药品、材料库存数据
drop table 临时_物资库存 purge;

create table 临时_物资库存 as 
select 机构编码,机构名称,代码,拆零比,
       sum(数量) as 库存数量,sum(数量*单价) as 库存金额,trunc(sum(数量*单价) / sum(数量),2) as 库存均价, 
      单位 库存单位, 业务期间
from(
select 机构编码,机构名称,国标代码 as 代码,拆零比,
       本期结存数 数量,本期结存数*购入价 as 金额,购入价 单价, 库存单位 单位, to_char(结转日期,'YYYYMM') as 业务期间
from 负荷_药库结转 where 机构编码 = 'H00000000000'
and substr(upper(国标代码),1,1) in ('C','X','Z')
union all
select 机构编码,机构名称,国标代码 as 代码,拆零比,
       本期结存数 数量,本期结存数*购入价 as 金额,购入价 单价, 库存单位 单位, to_char(结转日期,'YYYYMM') as 业务期间
from 负荷_药房结转 where 机构编码 = 'H00000000000'
and substr(upper(国标代码),1,1) in ('C','X','Z')
) a
group by 机构编码,机构名称,代码,拆零比,单位,业务期间
having sum(数量) <> 0;
