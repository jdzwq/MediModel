--*******************************************************************************************--
--脚本：将临时物资购入、销售数据归集至库房进销表
delete from 模型_库房进销 where 机构编码 = 'H00000000000';
commit;

INSERT INTO 模型_库房进销 (机构编码, 机构名称, 业务期间, 代码, 类别, 属性, 名称, 规格, 销售数量, 销售金额, 销售均价, 销售单位)
select 机构编码, 机构名称, 业务周期, 代码, 类别, 属性, 名称, 规格, 销售数量, 销售金额, 销售均价, 销售单位
from 临时_物资销售
ORDER BY 业务周期, 代码;
commit;

alter index 索引_模型_库房进销 rebuild;

--脚本：更新库房购入
begin
  for cur in (
    select a.rowid, b.购入数量,b.购入金额,b.购入均价,b.购入单位,b.拆零比
    from 模型_库房进销 a,临时_物资购入 b 
    where a.机构编码 = 'H00000000000' and a.机构编码 = b.机构编码 and a.业务期间 = b.业务期间 and a.代码 = b.代码
    ) loop
    update 模型_库房进销 
    set 购入数量 = cur.购入数量,
    购入金额 = cur.购入金额,
    购入均价 = cur.购入均价,
    购入单位 = cur.购入单位,
    拆零比 = cur.拆零比 where rowid = cur.rowid;
    end loop;
  commit;
end;

--脚本：更新库房库存
begin
  for cur in (
    select a.rowid, b.库存数量,b.库存金额,b.库存均价,b.库存单位 from 模型_库房进销 a,临时_物资库存 b 
    where a.机构编码 = 'H00000000000' and a.机构编码 = b.机构编码 and a.业务期间 = b.业务期间 and a.代码 = b.代码
    ) loop
    update 模型_库房进销 
    set 库存数量 = cur.库存数量,
    库存金额 = cur.库存金额,
    库存均价 = cur.库存均价,
    库存单位 = cur.库存单位 where rowid = cur.rowid;
    end loop;
  commit;
end;
