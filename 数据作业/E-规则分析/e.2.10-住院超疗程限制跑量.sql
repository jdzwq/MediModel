select a.机构编码,a.代码,a.身份证号,a.住院日期,sum(a.人次) 人次
from 统计_住院频度 a where a.机构编码 = 'H33018201141' and a.类别 in ('西药费','成药费')
and exists (select 1 from 规则_药品限制 t where upper(a.代码) like t.药品编码 || '%' and t.疗程限制 is not null)
group by a.机构编码,a.代码,a.身份证号,a.住院日期
having sum(a.人次) > 0;

